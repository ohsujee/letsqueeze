export function getAvatarUrl(avatarId) {
  if (!avatarId) return null;
  // V2 avatars are SVGs in /images/avatars2/
  if (avatarId.startsWith('v2-')) {
    return `/images/avatars2/${avatarId.slice(3)}.svg`;
  }
  return `/images/avatars/${avatarId}.webp`;
}
