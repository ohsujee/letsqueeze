import type { StandupScript } from './index';

const standup: StandupScript = {
  id: 'standup_004',
  title: 'Le CV Honnête',
  tone: 'genant',
  difficulty: 'facile',
  duration: '2-3 min',
  stageDirections: 'Assis bien droit, comme à un entretien d\'embauche. Tient une feuille imaginaire. Lit avec un sérieux total qui contraste avec le contenu. Après la section Compétences techniques, CASSER le personnage : relire sa propre feuille avec horreur, puis tenter de se rattraper avec une énergie désespérée. Les Centres d\'intérêt sont débités à toute vitesse, comme pour s\'en débarrasser.',
  script: `[S'assoit, ajuste une cravate imaginaire, déplie une feuille]

Bonjour. Merci de me recevoir. J'ai apporté mon CV. Mais attention : le vrai. Pas celui qu'on envoie. Celui qu'on devrait envoyer.

[Lit avec application]

Nom : peu importe. Âge : trop vieux pour être "jeune et dynamique", trop jeune pour qu'on me prenne au sérieux. Photo : prise quatorze fois, gardé la seule où on dirait que je suis employable.

[Tourne la feuille, continue]

Formation. Baccalauréat obtenu. Mention : "ouf". Licence de trois ans faite en cinq. Master : j'ai dit que je l'avais sur LinkedIn, on va pas creuser. Un MOOC commencé en 2021. Jamais terminé. Mais j'ai le mail de bienvenue, si vous voulez une preuve.

[Continue, toujours sérieux]

Expérience professionnelle. Stage de six mois dans une entreprise dont j'ai oublié le nom. Tâches effectuées : sourire lors des réunions et dire "intéressant" toutes les quatre minutes. Bilan : zéro compétence acquise, mais un réseau LinkedIn de 847 personnes dont je ne connais personne. Référence disponible sur demande. Enfin... ne demandez pas.

[S'enfonce dans sa chaise]

Compétences techniques. Excel : je sais ouvrir le logiciel. Parfois. J'ai fait un graphique une fois, il ressemblait à un électrocardiogramme. Mon manager a cru que c'était une blague. C'était le bilan trimestriel. PowerPoint : j'ai fait un diaporama en 2019 avec des transitions "étoile". Mon patron a pleuré. Pas de joie. Anglais : "fluent" sur le CV, "the cat is on the table" dans la vraie vie.

[S'arrête net. Relit sa feuille en silence. Son visage se décompose.]

Attendez... je viens de relire ce que j'ai écrit. C'est... c'est mon vrai CV ça. C'est ça que je suis en train de lire à voix haute ? Devant vous ? Oh non. Oh non non non.

[Panique brève, puis se reprend avec une énergie désespérée, sourire forcé]

MAIS — compétences humaines ! C'est là que ça change ! Travail d'équipe : je suis le ROI du travail d'équipe. Je dis oui à tout. Absolument tout. "On fait une réunion à 7h ?" Oui. "Tu peux rester ce soir ?" Bien sûr. "Tu veux reprendre le projet de Gérard ?" Avec PLAISIR. Autonomie : personne ne me supervise. Personne ne sait ce que je fais. C'est la même chose, non ? Gestion du stress : je pleure dans les toilettes mais SILENCIEUSEMENT, donc techniquement, ça ne dérange personne.

[Le sourire forcé s'effondre, débite la section suivante à toute vitesse]

Centres d'intérêt. Normalement on met "voyages, lecture, sport". Soyons honnêtes. Scroller mon téléphone au lit jusqu'à ce que mon bras s'engourdisse. Regarder des vidéos de 45 secondes pendant trois heures. Ouvrir le frigo toutes les vingt minutes en espérant qu'un plat se soit matérialisé. Terminé.

[Range la feuille, silence]

Disponibilité : immédiate. Prétentions salariales : à ce stade, un sourire et un ticket restaurant.

[Tend la main pour serrer celle du recruteur. Pause.]

Vous me rappelez ? ... Non ? C'est parce que j'ai dit le coup d'Excel, hein. C'est toujours Excel qui me trahit.

[Se lève, ramasse ses affaires. S'arrête à la porte.]

Je vous ai menti. Sur une seule chose. Mon niveau d'Excel. C'est pire.

[Sort avec dignité]`,
};

