import type { StandupScript } from './index';

const standup: StandupScript = {
  id: 'standup_002',
  title: 'Le Sommelier d\'Eau',
  tone: 'cringe',
  difficulty: 'moyen',
  duration: '2-3 min',
  pro: true,
  stageDirections: 'Jouer un sommelier extrêmement prétentieux. Faire tourner un verre imaginaire, renifler, goûter avec des bruits de dégustation exagérés. Confiance absolue.',
  script: `[S'avance avec un verre imaginaire, le fait tourner délicatement]

Bonsoir. Je suis Thibault de la Roche-Posay — aucun lien avec la marque, quoique — et je suis sommelier. Mais pas n'importe quel sommelier.

[Renifle le verre avec concentration]

Sommelier en eau.

Alors ce soir, nous allons commencer par une petite eau du robinet. Millésime... 2026. Station d'épuration de Bobigny. Très bon cru.

[Goûte avec des bruits de dégustation absurdes]

Hmmm. Alors. En bouche, on a une attaque franche, légèrement calcaire. Des notes de tuyauterie en cuivre, avec un soupçon de... joint en caoutchouc des années 90. La finale est longue, un peu chlorée, avec un arrière-goût de "est-ce que c'est vraiment potable ?"

[Note dans un carnet imaginaire]

Je lui mets 14 sur 20. Buvable, mais sans génie.

[Prend un autre verre imaginaire]

Maintenant. Celle-ci est plus rare. Eau de pluie, récoltée à la main sur un balcon du 93 un jeudi de novembre. Un terroir exceptionnel.

[Goûte, ferme les yeux]

Ah ! Là, on est sur autre chose. Notes de pollution atmosphérique, pointe de kérosène — on sent le passage de l'A380 des 14h30. C'est aérien. C'est... presque spirituel.

[Prend un troisième verre avec révérence]

Et pour finir, la pièce de résistance. L'eau que tout le monde a goûtée mais que personne n'ose analyser.

[Pause dramatique]

L'eau des pâtes.

[Goûte longuement, les yeux écarquillés]

Mon Dieu. C'est... c'est bouleversant. On a l'amidon qui tapisse le palais comme une couverture un soir d'hiver. Des notes de sel — il en a mis trop, comme d'habitude. Et en arrière-plan, la nostalgie d'une Barilla numéro 5 al dente.

[S'essuie une larme]

Cette eau raconte une histoire. L'histoire d'un homme seul, un mardi soir, qui n'avait pas envie de cuisiner mais qui l'a fait quand même. C'est du courage liquide.

[Pose le verre, solennel]

17 sur 20. Mais retirez un point si c'étaient des coquillettes.`,
};

export default standup;
