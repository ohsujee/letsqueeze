import type { StandupScript } from './index';

const standup: StandupScript = {
  id: 'standup_010',
  title: 'Le Guide Michelin de la Cantine',
  tone: 'cringe',
  difficulty: 'moyen',
  duration: '2-3 min',
  stageDirections: 'Inspecteur Michelin. Sérieux impénétrable. Prendre des notes dans un carnet. Examiner la nourriture imaginaire comme un bijou rare. Dévasté mais professionnel.',
  pro: true,
  script: `[S'assoit, déplie une serviette imaginaire sur ses genoux, sort un carnet]

Jour 1. Cantine d'entreprise. Bâtiment C. 12h15.

L'accueil est... fonctionnel. On nous remet un plateau en plastique orange — couleur choisie, j'imagine, pour masquer les traces de sauce. Ingénieux.

[Examine un plat imaginaire]

En entrée, une salade qualifiée sur l'ardoise de "composée". Composée de quoi, mystère. J'identifie de la laitue fatiguée — pas fatiguée comme un chef après le service, fatiguée comme quelqu'un qui a renoncé. Un quart de tomate qui a vu des choses. Et trois grains de maïs, placés là comme un cri de détresse.

[Note dans le carnet]

Assaisonnement : une vinaigrette industrielle servie dans un ramequin. J'ai goûté. C'est du vinaigre qui a entendu parler de l'huile d'olive mais qui ne l'a jamais rencontrée.

[On lui apporte le plat principal imaginaire]

Le plat de résistance. Et croyez-moi, il porte bien son nom : il faut de la résistance pour le manger.

C'est présenté comme un "émincé de volaille sauce forestière". Décortiquons. Le mot "émincé" suggère qu'on a émincé quelque chose. Ce n'est pas le cas. Ce sont des cubes. De la volaille cubiste. Picasso serait fier, mais mon palais est en deuil.

[Goûte avec cérémonie]

La sauce forestière... Alors. "Forestière" implique des champignons. J'en vois un. Un seul. Il flotte au milieu de l'assiette comme un naufragé. Autour de lui, un liquide marron que je qualifierais de "jus de mystère". C'est chaud. C'est marron. C'est tout ce qu'on peut affirmer.

[Goûte l'accompagnement]

L'accompagnement : des pâtes. Des coquillettes. Cuisson al dente ? Non. Cuisson al "abandonne". Elles ont fusionné entre elles pour former un bloc compact. Ce n'est plus un féculent. C'est du ciment. On pourrait bâtir une maison avec.

[Se tourne vers le dessert]

Et le dessert. Ah, le dessert. Un yaourt aux fruits. Fruits au pluriel, et pourtant. J'ai cherché le fruit. Longtemps. J'ai trouvé un fragment rose au fond du pot. Fraise ? Framboise ? Souvenir d'un été lointain ? Impossible à dire.

[Referme le carnet, pose son stylo]

Verdict final. Sur l'échelle Michelin, je décerne à cette cantine... zéro étoile. Mais je lui accorde un Bib Gourmand de la Survie, parce que les gens reviennent. Chaque jour. Malgré tout. Et ça, c'est peut-être la plus belle preuve de courage que j'ai vue dans toute ma carrière.

[Repose la serviette]

L'addition s'élève à 3,80 euros. C'est le juste prix de la souffrance.`,
};

